    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        if (!jt_modCarreraAlum.getText().isBlank() && (int) js_modclases.getValue() > 0 && !jt_modCuentaAlumno.getText().isBlank() && !jt_modpassAlumno.getText().isBlank()
                && !jt_modUserAlumno.getText().isBlank() && !jt_modnameAlumno.getText().isBlank()) {
            ((Alumno) base.get(jl_todomod.getSelectedIndex())).setUser(jt_modUserAlumno.getText());
            ((Alumno) base.get(jl_todomod.getSelectedIndex())).setCarrera(jt_modCarreraAlum.getText());
            ((Alumno) base.get(jl_todomod.getSelectedIndex())).setNombre(jt_modnameAlumno.getText());
            ((Alumno) base.get(jl_todomod.getSelectedIndex())).setPass(jt_modpassAlumno.getText());
            ((Alumno) base.get(jl_todomod.getSelectedIndex())).setCuenta(jt_modCuentaAlumno.getText());
            ((Alumno) base.get(jl_todomod.getSelectedIndex())).setClasescursadas((int) js_modclases.getValue());
            jt_modUserAlumno.setText("");
            jt_modCarreraAlum.setText("");
            jt_modCuentaAlumno.setText("");
            jt_modnameAlumno.setText("");
            jt_modpassAlumno.setText("");
        } else if (!jt_modCarreraAlum.getText().isBlank()) {
            ((Alumno) base.get(jl_todomod.getSelectedIndex())).setCarrera(jt_modCarreraAlum.getText());
            jt_modCarreraAlum.setText("");
            JOptionPane.showMessageDialog(this, "Modificado con exito");
        } else if (!jt_modnameAlumno.getText().isBlank()) {
            ((Alumno) base.get(jl_todomod.getSelectedIndex())).setNombre(jt_modnameAlumno.getText());
            jt_modnameAlumno.setText("");
            JOptionPane.showMessageDialog(this, "Modificado con exito");
        } else if (!jt_modpassAlumno.getText().isBlank()) {
            ((Alumno) base.get(jl_todomod.getSelectedIndex())).setPass(jt_modpassAlumno.getText());
            JOptionPane.showMessageDialog(this, "Modificado con exito");
            jt_modpassAlumno.setText("");
        } else if (!jt_modCuentaAlumno.getText().isBlank()) {
            ((Alumno) base.get(jl_todomod.getSelectedIndex())).setCuenta(jt_modCuentaAlumno.getText());
            jt_modCuentaAlumno.setText("");
            JOptionPane.showMessageDialog(this, "Modificado con exito");
        } else if ((int) js_modclases.getValue() > 0) {
            ((Alumno) base.get(jl_todomod.getSelectedIndex())).setClasescursadas((int) js_modclases.getValue());
            JOptionPane.showMessageDialog(this, "Modificado con exito");
        } else if (!jt_modUserAlumno.isValidateRoot()) {
            ((Alumno) base.get(jl_todomod.getSelectedIndex())).setUser(jt_modUserAlumno.getText());
            JOptionPane.showMessageDialog(this, "Modificado con exito");
            jt_modUserAlumno.setText("");

        } else {
            JOptionPane.showMessageDialog(this, "Necesita llenar una casilla o todas");
        }
        au.setList(base);
        au.escribirArchivo();
    }//GEN-LAST:event_jButton3MouseClicked

