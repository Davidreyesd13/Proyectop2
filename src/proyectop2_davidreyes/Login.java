    private void jb_verificarexamenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_verificarexamenMouseClicked
        // TODO add your handling code here:
        int primera = 0;
        flag = true;
        for (int i = 0; i < base.size(); i++) {
            if (usuario.equals(((Alumno) base.get(i)).getUser())) {
                primera = i;
            }
        }
        if (((Alumno) base.get(primera)).getClases().isEmpty() || ((Alumno) base.get(primera)).getClases().get(jl_clasesalum.getSelectedIndex()).getEx().isEmpty()) {
            flag = false;
        }

        if (flag) {
            jf_hacerexamen.pack();
            jf_hacerexamen.setLocationRelativeTo(this);
            jf_hacerexamen.setVisible(true);
            jf_alumnointerface.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "No hay examenes pendientes");
        }
    }//GEN-LAST:event_jb_verificarexamenMouseClicked

